        /// </summary>
        [Required]
        [StringLength(16)]
        public string TransactionID { get; set; }
        /// <summary>
        /// Gets or sets the transaction status.
        /// </summary>
        [Required]
        [StringLength(32)]
        public string Status { get; set; }
        /// <summary>
        /// Gets or sets the status request date time stamp.
        /// </summary>
        public DateTime StatusRequestDateTimeStamp { get; set; }
        /// <summary>
        /// Gets or sets the status response date time stamp.
        /// </summary>
        public DateTime StatusResponseDateTimeStamp { get; set; }
        /// <summary>
        /// Gets or sets the status date time stamp.
        /// </summary>
        public DateTime? StatusDateTimeStamp { get; set; }
        /// <summary>
        /// Gets or sets the transaction.
        /// </summary>
        public virtual p_iDealTransaction Transaction { get; set; }
    }
}
