namespace BND.Services.Payments.PaymentIdInfo.Models
{
    using System;
    using System.ComponentModel.DataAnnotations;
    using System.ComponentModel.DataAnnotations.Schema;

    /// <summary>
    /// Class p_TransactionStatusHistory.
    /// </summary>
    [Table("ideal.TransactionStatusHistory")]
    public partial class p_iDealTransactionStatusHistory
    {
